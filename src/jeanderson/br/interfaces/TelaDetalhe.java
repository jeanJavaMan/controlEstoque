    private void preencherDesc(int posicao) {
        this.posicao = posicao;
        Main main = new Main();
        txtNome.setEditable(false);
        txtQuantidade.setEditable(false);
        txtValor.setEditable(false);
        txtDescricao.setEditable(false);
        txtNome.setText(main.getEstoque().get(posicao).getNome());
        txtQuantidade.setText(Integer.toString(main.getEstoque().get(posicao).getQuantidade()));
        txtValor.setText(Double.toString(main.getEstoque().get(posicao).getValor()));
        txtDescricao.setText(main.getEstoque().get(posicao).getDescricao());
    }

