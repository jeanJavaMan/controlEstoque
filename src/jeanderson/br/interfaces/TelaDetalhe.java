    private void btnDescriçãoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDescriçãoActionPerformed
        if (!editou) {
            txtDescricao.setEditable(true);
            editou = true;
            btnDescrição.setText("Concluir Alteração");
        } else {
            Main main = new Main();
            main.getEstoque().get(posicao).setDescricao(txtDescricao.getText());
            main.atualizarBanco();
            editou = false;
            btnDescrição.setText("Editar Descrição");
            txtDescricao.setEditable(false);
        }
    }//GEN-LAST:event_btnDescriçãoActionPerformed

