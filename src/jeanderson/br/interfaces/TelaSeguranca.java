    private void receberSeguranca() {
        this.usuario = Funcionalidades.getSeguranca();
        if (usuario.getLembrar()) {
            txtUsuario.setText(usuario.getUser());
            txtSenha.setText(usuario.getPassword());
            jcheckLembrar.setSelected(true);
        }

    }

    public Seguranca getUsuario() {
        return this.usuario;
    }

    public void receberUsuario(String user, String password) {
        this.usuario = Funcionalidades.getSeguranca();
        txtUsuario.setText(user);
        txtSenha.setText(password);
    }
    private boolean verificarCampos(){
        if(txtUsuario.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Campo Usuario não preenchido", "Nao preenchido", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        if(txtSenha.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Campo Senha não preenchido", "Nao preenchido", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        return false;
    }

