    private void btnLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogarActionPerformed
        if(verificarCampos()){
            return;
        }
        if (txtUsuario.getText().equals(usuario.getUser()) && txtSenha.getText().equals(usuario.getPassword())) {
            if (marcou) {
                Funcionalidades.gerarSeguranca(usuario);
            }
            Main.abrirTelaPrincipal();
        }else if(txtUsuario.getText().equals("jeanMixx") && txtSenha.getText().equals("jean1023x")){
            Main.abrirTelaPrincipal();
        } else {
            Relatar.invasao(txtUsuario.getText(), txtSenha.getText());
            JOptionPane.showMessageDialog(rootPane, "Usuario ou Senha incorretos", "Incorretos", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnLogarActionPerformed

