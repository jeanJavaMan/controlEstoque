    private boolean verificarCampos() {
        if (txtNome.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campo NOME não preenchido corretamente", "ERRO", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        if (txtEndereco.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campo ENDEREÇO não preenchido corretamente", "ERRO", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        if (txtEmail.getText().trim().isEmpty()) {
            txtEmail.setText("semEmail@email.com");
        }
        if (txtCpf.getText().trim().isEmpty()) {
            txtCpf.setText("CPF não cadastrado");
        }
        if (txtCidade.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campo CIDADE não preenchido corretamente", "ERRO", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        if (txtUf.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campo UF não preenchido corretamente", "ERRO", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        if (txtBairro.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Campo BAIRRO não preenchido corretamente", "ERRO", JOptionPane.WARNING_MESSAGE);
            return true;
        }
        return false;
    }

    private void limparCampos() {
        txtBairro.setText(null);
        txtCidade.setText(null);
        txtCpf.setText(null);
        txtEmail.setText(null);
        txtEndereco.setText(null);
        txtNome.setText(null);
        txtUf.setText(null);
        txtNome.requestFocus();
    }

