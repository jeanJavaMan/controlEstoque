    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        if (verificarCampos()) {
            return;
        }
        Clientes clientes = new Clientes(txtNome.getText(), txtCpf.getText(), txtEmail.getText(), txtEndereco.getText(), txtCidade.getText(), txtUf.getText(), txtBairro.getText());
        if (!main.adicionarCliente(clientes)) {
            main.atualizarListaClientes();
            limparCampos();
            JOptionPane.showMessageDialog(rootPane, "Cliente Cadastrado com Sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Já existe este Cliente Cadastrado", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

